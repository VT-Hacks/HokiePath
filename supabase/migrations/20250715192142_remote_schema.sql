CREATE OR REPLACE FUNCTION "public"."match_courses"("query_embedding" "public"."vector", "match_count" integer DEFAULT 5) RETURNS TABLE("course_id" "uuid", "code" "text", "subject" "text", "title" "text", "similarity" double precision)
    LANGUAGE "sql" STABLE
    AS $$
SELECT 
  c.id AS course_id,
  c.code,
  c.title,
  c.subject_code,
  1 - (e.embedding <=> query_embedding) AS similarity
FROM course_embeddings e
JOIN courses c ON c.id = e.course_id
ORDER BY e.embedding <=> query_embedding
LIMIT match_count;
$$;


ALTER FUNCTION "public"."match_courses"("query_embedding" "public"."vector", "match_count" integer) OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."match_courses"("query_embedding" "public"."vector", "match_count" integer DEFAULT 5, "filter" "jsonb" DEFAULT '{}'::"jsonb") RETURNS TABLE("content" "text", "metadata" "jsonb")
    LANGUAGE "plpgsql" STABLE
    AS $$
BEGIN
  -- Set ivfflat probes per function call for better recall
  PERFORM set_config('ivfflat.probes', '6', true);

  RETURN QUERY
  SELECT
    e.content,
    e.metadata
  FROM course_embeddings e
  WHERE e.metadata @> filter
  ORDER BY e.embedding <=> query_embedding
  LIMIT match_count;
END;
$$;


